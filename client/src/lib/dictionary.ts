// Build the dictionary map
export const DICTIONARY = new Map<string, DictEntry>();

for (const [hanzi, pinyin, definition, hsk] of RAW_DICT) {
  if (!DICTIONARY.has(hanzi)) {
    DICTIONARY.set(hanzi, { hanzi, pinyin, definition, hsk });
  }
}

// ─── Segmentation Engine ──────────────────────────────────────────────────────

export interface Segment {
  text: string;
  entry: DictEntry | null;
  start: number;
  end: number;
}

/**
 * Longest-match (forward maximum matching) tokenizer.
 * Always tries the longest possible match first.
 * If no match found, flags the character instead of guessing.
 * Multi-character compounds are never split if they exist in dictionary.
 */
export function segmentText(text: string): Segment[] {
  const segments: Segment[] = [];
  let i = 0;
  const maxLen = 10;

  while (i < text.length) {
    const char = text[i];

    if (!isChinese(char)) {
      segments.push({ text: char, entry: null, start: i, end: i + 1 });
      i++;
      continue;
    }

    let matched = false;
    const tryLen = Math.min(maxLen, text.length - i);

    for (let len = tryLen; len >= 1; len--) {
      const candidate = text.slice(i, i + len);
      const entry = DICTIONARY.get(candidate);
      if (entry) {
        segments.push({ text: candidate, entry, start: i, end: i + len });
        i += len;
        matched = true;
        break;
      }
    }

    if (!matched) {
      segments.push({ text: char, entry: null, start: i, end: i + 1 });
      i++;
    }
  }

  return segments;
}

export function isChinese(char: string): boolean {
  const code = char.charCodeAt(0);
  return (
    (code >= 0x4e00 && code <= 0x9fff) ||
    (code >= 0x3400 && code <= 0x4dbf) ||
    (code >= 0x20000 && code <= 0x2a6df) ||
    (code >= 0xf900 && code <= 0xfaff) ||
    (code >= 0x2f800 && code <= 0x2fa1f)
  );
}

export function lookupWord(hanzi: string): DictEntry | null {
  if (!hanzi) return null;
  const normalized = hanzi.trim().replace(/[\u3000\u00a0\ufeff]/g, '').normalize('NFC');
  const key = normalized || hanzi.trim();

  // ── Try CC-CEDICT first (120k entries, much better coverage) ──────────────
  // cedictLookup is a lazy import to avoid circular deps — we call it via the
  // global singleton that cedict.ts exposes once loaded.
  try {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    const cedict = (globalThis as any).__CEDICT_MAP__ as Map<string, { pinyin: string; definition: string }> | undefined;
    if (cedict) {
      const cedictEntry = cedict.get(key);
      if (cedictEntry) {
        return { hanzi: key, pinyin: cedictEntry.pinyin, definition: cedictEntry.definition };
      }
    }
  } catch { /* cedict not loaded yet — fall through */ }

  // ── Fall back to local curated dictionary ─────────────────────────────────
  return DICTIONARY.get(key) ?? DICTIONARY.get(hanzi.trim()) ?? null;
}

export function searchDictionary(query: string): DictEntry[] {
  const results: DictEntry[] = [];
  const lq = query.toLowerCase();
  DICTIONARY.forEach((entry, hanzi) => {
    if (hanzi.includes(query) || entry.pinyin.includes(query) || entry.definition.toLowerCase().includes(lq)) {
      results.push(entry);
    }
  });
  return results.slice(0, 20);
}
